export enum DateFormatEnum {
  /** @example '2024-06-01' */
  YYYYMMDD = "YYYY-MM-DD",

  /** @example '2024-06-01 11:45:23' */
  YYYYMMDDHHmmss = "YYYY-MM-DD HH:mm:ss",

  /** @example '24-6-1 1:15:3' */
  YYMDHms = "YY-M-D H:m:s",

  /** @example '24-6-1' */
  YYMD = "YY-M-D",

  /** @example '13:07:55' */
  HHmmss = "HH:mm:ss",
}
